# A string value is assigned to variable a.
a = 'int'

# An integer value is assigned to variable b.
b = 7

# A boolean value is assigned to variable c.
c = False 

# A string value is assigned to variable d.
d = "18.5" 

# shows the types of the variables a, b, c and d.
print("Variable a is of type:", type(a))
print("Variable b is of type:", type(b))
print("Variable c is of type:", type(c))
print("Variable d is of type:", type(d))

# changes the type of variables b and d to an float and adds them together, this result is the value of variable x.
x = float(b) + float(d) 

# shows the value of variable x.
print(x)
